


    string choiceNumToString (int choice_num) {
        // variable
        string result = "";
        // if statement to convert and return it
        if (choice_num = 1) {
            result = STR_ROCK;
        } else if (choice_num = 2) {
            result = STR_PAPER;
        } else if (choice_num = 3) {
            result = STR_SCISSORS;
        }
    return result;
}
